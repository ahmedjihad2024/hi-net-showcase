extension CurrTra on Currency {
  String get tr => 'currency_name.$name'.tr();
}

extension Tra on Translation {
  String get tr => name.tr();
  String trNamed(Map<String, String> params) => name.tr(namedArgs: params);
  String trDay(int days) => 'time.day'.tr(namedArgs: {'num': days.toString()});
  String trWeek(int week) =>
      'time.week'.tr(namedArgs: {'num': week.toString()});
}
